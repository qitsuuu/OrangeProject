print("loop through and print items in a list")
prefectures = ["Osaka", "Aomori", "Fukuoka", "Tokyo"]
for item in prefectures:
    print(item)

print("loop through and print index in range based on length")
for index in range(len(prefectures)):
    print(index)

print("loop through and print items in range based on length")
for index in range(len(prefectures)):
    print(prefectures[index])

print("loop through and print items in range")
for index in range(4, 10):
    print(index)